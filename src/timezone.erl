%%--------------------------------------------------------------------------------------------------
-module(timezone).
%%--------------------------------------------------------------------------------------------------
-include("../include/datetime.hrl").
%%--------------------------------------------------------------------------------------------------
-export([
  new/0
]).
%%--------------------------------------------------------------------------------------------------

%% @doc Returns the datetime() representation of now().
-spec new() -> #datetime{}.
 	      
new() ->
  {Date,Time} = calendar:now_to_datetime( erlang:now() ),
  #datetime{ date = date:new(Date), time = time:new(Time) }.
  
